# 字符串类型
name = "Kim"
# 整数类型
age = 28
# 浮点数类型
weight = 143.5
# 布尔型
is_boy = True

print(name)
print(age)
print(weight)
print(is_boy)

# type函数用于得到变量的数据类型
type_of_name = type(name)
type_of_age = type(age)
type_of_weight = type(weight)
type_of_is_boy = type(is_boy)

print(type_of_name)
print(type_of_age)
print(type_of_weight)
print(type_of_is_boy)

# 也可以直接函数套用
print(type(name))
print(type(age))
print(type(weight))
print(type(is_boy))